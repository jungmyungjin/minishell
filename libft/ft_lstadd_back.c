#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*old_last;

	old_last = *lst;
	if (old_last == NULL)
	{
		*lst = new;
	}
	else
	{
		while (old_last->next != NULL)
			old_last = old_last->next;
		old_last->next = new;
	}
}
