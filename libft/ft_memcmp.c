#include "libft.h"

int	ft_memcmp(const void *b1, const void *b2, size_t len)
{
	size_t	index;

	index = 0;
	while (index < len)
	{
		if (((unsigned char *)b1)[index] != ((unsigned char *)b2)[index])
			return (((unsigned char *)b1)[index] - ((unsigned char *)b2)
				[index]);
		index++;
	}
	return (0);
}
