#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	int		index;
	char	*local_dest;

	index = 0;
	local_dest = (char *)b;
	while ((unsigned long)index < len)
	{
		local_dest[index] = 0;
		index++;
	}
	return (local_dest);
}
