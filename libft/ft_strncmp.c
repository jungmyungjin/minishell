#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, int len)
{
	int	i;

	i = 0;
	while (i < len)
	{
		if ((unsigned char)s1[i] != (unsigned char)s2[i] || (!s1[i] || !s2[i]))
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}
