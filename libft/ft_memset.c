#include "libft.h"

void	*ft_memset(void *dest, int c, size_t len)
{
	int		index;
	char	*local_dest;

	if (!dest)
		return (dest);
	index = 0;
	local_dest = (char *)dest;
	while ((unsigned long)index < len)
	{
		local_dest[index] = c;
		index++;
	}
	return (local_dest);
}
